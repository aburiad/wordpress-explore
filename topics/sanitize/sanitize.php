Input Sanitization হল WordPress-এর একটি গুরুত্বপূর্ণ পদ্ধতি, যা ব্যবহারকারীর ইনপুট ডেটা সঠিকভাবে পরিষ্কার করে। এটি ডেটা প্রসেস করার সময় অবাঞ্ছিত বা ক্ষতিকারক উপাদান দূর করে এবং সাইটকে নিরাপদ রাখতে সাহায্য করে।

কেন Sanitization গুরুত্বপূর্ণ?
সিকিউরিটি বাড়ানো:
SQL ইনজেকশন, XSS (Cross-Site Scripting), এবং অন্যান্য আক্রমণ এড়াতে।

ডেটা মান নিশ্চিত করা:
ইউজারের ইনপুট সঠিক ফরম্যাটে আছে তা নিশ্চিত করা।

ভুল ডেটা এড়ানো:
অবাঞ্ছিত বা অপ্রয়োজনীয় ডেটা ইনপুট থেকে ফিল্টার করা।



১. sanitize_text_field()
$name = sanitize_text_field($_POST['name']);

HTML ট্যাগ মুছে ফেলে।
অপ্রয়োজনীয় স্পেস এবং লাইন ব্রেক সরিয়ে দেয়।



২. sanitize_email()
ইমেইল ইনপুট স্যানিটাইজ করার জন্য।
$email = sanitize_email($_POST['email']);
শুধুমাত্র বৈধ ইমেইল ফরম্যাট রেখে দেয়।



৩. esc_url_raw() এবং sanitize_url()
$url = esc_url_raw($_POST['website']);
অপ্রয়োজনীয় বা ক্ষতিকারক ক্যারেক্টার মুছে ফেলে।
বৈধ URL রেখে দেয়।


৪. intval()
সংখ্যা ইনপুট স্যানিটাইজ করতে।
$age = intval($_POST['age']);
ইনপুট থেকে শুধুমাত্র পূর্ণসংখ্যা রাখে।


৬. sanitize_key()
ডাটাবেজ বা কনফিগ ফাইলের জন্য কী স্যানিটাইজ করতে।
$key = sanitize_key($_POST['option_key']);
অক্ষর, সংখ্যা, ড্যাশ (-), এবং আন্ডারস্কোর (_) রেখে দেয়।


৭. sanitize_file_name()
ফাইল নাম স্যানিটাইজ করতে।
$file_name = sanitize_file_name($_FILES['file']['name']);




Sanitization vs Validation

-----------------------------------------
Sanitization:
ইনপুট থেকে অবাঞ্ছিত বা ক্ষতিকারক ডেটা সরিয়ে রাখা।

Validation:
ইনপুট সঠিক কিনা তা যাচাই করা।


উদাহরণ:
$email = sanitize_email($_POST['email']);
if (!is_email($email)) {
    echo "Invalid email address!";
}



